class TestListExperimentsFilter:
    """Test list_experiments filtering and sorting."""

    def _filter_experiments(self, exps, status=None, milestone=None, tag=None):
        """Replicate list_experiments filtering."""
        result = list(exps)
        if status:
            result = [e for e in result if e.status == status]
        if milestone:
            result = [e for e in result if e.roadmap_milestone == milestone]
        if tag:
            result = [e for e in result if tag in e.tags]
        result.sort(key=lambda x: x.created_at, reverse=True)
        return result

    def test_no_filters_returns_all(self):
        """No filters returns all experiments."""
        exps = [
            Experiment(id="1", name="E1", created_at="2026-04-01T10:00:00"),
            Experiment(id="2", name="E2", created_at="2026-04-02T10:00:00"),
        ]
        result = self._filter_experiments(exps)
        assert len(result) == 2

    def test_filter_by_status(self):
        """Status filter keeps matching experiments."""
        exps = [
            Experiment(id="1", name="E1", status="running"),
            Experiment(id="2", name="E2", status="completed"),
            Experiment(id="3", name="E3", status="running"),
        ]
        result = self._filter_experiments(exps, status="running")
        assert len(result) == 2
        assert {e.id for e in result} == {"1", "3"}

    def test_filter_by_milestone(self):
        """Milestone filter keeps matching experiments."""
        exps = [
            Experiment(id="1", name="E1", roadmap_milestone="phase1"),
            Experiment(id="2", name="E2", roadmap_milestone="phase2"),
        ]
        result = self._filter_experiments(exps, milestone="phase1")
        assert len(result) == 1
        assert result[0].id == "1"

    def test_filter_by_tag(self):
        """Tag filter keeps matching experiments."""
        exps = [
            Experiment(id="1", name="E1", tags=["nlp"]),
            Experiment(id="2", name="E2", tags=["cv"]),
            Experiment(id="3", name="E3", tags=["nlp", "transformer"]),
        ]
        result = self._filter_experiments(exps, tag="nlp")
        assert len(result) == 2
        assert {e.id for e in result} == {"1", "3"}

    def test_sort_by_created_at_descending(self):
        """Results sorted by created_at descending (newest first)."""
        exps = [
            Experiment(id="1", name="E1", created_at="2026-04-01T10:00:00"),
            Experiment(id="2", name="E2", created_at="2026-04-10T10:00:00"),
            Experiment(id="3", name="E3", created_at="2026-04-05T10:00:00"),
        ]
        result = self._filter_experiments(exps)
        assert result[0].id == "2"  # newest
        assert result[1].id == "3"
        assert result[2].id == "1"  # oldest

    def test_combined_filters(self):
        """Multiple filters can be combined."""
        exps = [
            Experiment(id="1", name="E1", status="running", tags=["nlp"]),
            Experiment(id="2", name="E2", status="completed", tags=["nlp"]),
            Experiment(id="3", name="E3", status="running", tags=["cv"]),
        ]
        result = self._filter_experiments(exps, status="running", tag="nlp")
        assert len(result) == 1
        assert result[0].id == "1"


