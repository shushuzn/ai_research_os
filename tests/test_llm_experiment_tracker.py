class TestCompareLogic:
    """Test compare() logic."""

    def _compare(self, exps, metric_names=None):
        """Replicate compare logic."""
        if not exps:
            return {"error": "No experiments found"}
        if not metric_names:
            metric_names = list(set(m.name for e in exps for m in e.metrics))
        rows = []
        for e in exps:
            row = {"id": e.id, "name": e.name, "status": e.status}
            for mn in metric_names:
                for m in e.metrics:
                    if m.name == mn:
                        row[mn] = m.value
                        break
            rows.append(row)
        return {"metrics": metric_names, "experiments": rows}

    def test_no_experiments_returns_error(self):
        """Empty list returns error."""
        result = self._compare([])
        assert "error" in result

    def test_no_metric_names_collected_from_experiments(self):
        """No metric_names → collect from all experiments."""
        exps = [
            Experiment(id="1", name="E1", metrics=[Metric(name="accuracy", value=0.9)]),
            Experiment(id="2", name="E2", metrics=[Metric(name="f1", value=0.8)]),
        ]
        result = self._compare(exps)
        assert "accuracy" in result["metrics"]
        assert "f1" in result["metrics"]

    def test_explicit_metric_names(self):
        """Explicit metric_names only shows those."""
        exps = [
            Experiment(id="1", name="E1", metrics=[Metric(name="accuracy", value=0.9), Metric(name="f1", value=0.8)]),
        ]
        result = self._compare(exps, metric_names=["accuracy"])
        assert result["metrics"] == ["accuracy"]
        assert result["experiments"][0]["accuracy"] == 0.9
        assert "f1" not in result["experiments"][0]

    def test_missing_metric_is_not_in_row(self):
        """Experiment without requested metric has no key for it."""
        exps = [
            Experiment(id="1", name="E1", metrics=[Metric(name="accuracy", value=0.9)]),
            Experiment(id="2", name="E2", metrics=[]),
        ]
        result = self._compare(exps, metric_names=["accuracy"])
        assert "accuracy" in result["experiments"][0]
        assert "accuracy" not in result["experiments"][1]

    def test_experiment_includes_id_name_status(self):
        """Each row includes id, name, status."""
        exps = [
            Experiment(id="e123", name="Best Exp", status="completed"),
        ]
        result = self._compare(exps)
        row = result["experiments"][0]
        assert row["id"] == "e123"
        assert row["name"] == "Best Exp"
        assert row["status"] == "completed"


