class TestExperimentDictConversion:
    """Test to_dict and from_dict."""

    def test_to_dict_includes_all_fields(self):
        """to_dict returns all fields."""
        e = Experiment(
            id="d",
            name="Dict Test",
            description="Desc",
            roadmap_milestone="m1",
            hypothesis_id="h1",
            config={"k": "v"},
            results={"r": 1},
            metrics=[Metric(name="m", value=0.5, unit="%")],
            status="running",
            created_at="2026-01-01T10:00:00",
            completed_at="2026-01-02T10:00:00",
            artifacts=["a.txt"],
            tags=["test"],
        )
        d = e.to_dict()
        assert d["id"] == "d"
        assert d["name"] == "Dict Test"
        assert d["description"] == "Desc"
        assert d["hypothesis_id"] == "h1"
        assert d["config"] == {"k": "v"}
        assert d["metrics"][0]["name"] == "m"
        assert d["status"] == "running"
        assert d["artifacts"] == ["a.txt"]
        assert d["tags"] == ["test"]

    def test_from_dict_reconstructs_experiment(self):
        """from_dict reconstructs experiment from dict."""
        data = {
            "id": "from",
            "name": "From Dict",
            "description": "",
            "roadmap_milestone": "",
            "hypothesis_id": "",
            "config": {},
            "results": {},
            "metrics": [{"name": "acc", "value": 0.9, "unit": "", "timestamp": ""}],
            "status": "completed",
            "created_at": "2026-01-01T10:00:00",
            "completed_at": "2026-01-02T10:00:00",
            "artifacts": [],
            "tags": [],
        }
        e = Experiment.from_dict(data)
        assert e.id == "from"
        assert e.name == "From Dict"
        assert e.status == "completed"
        assert len(e.metrics) == 1
        assert e.metrics[0].name == "acc"
        assert e.metrics[0].value == 0.9

    def test_from_dict_metrics_are_metric_objects(self):
        """from_dict converts metric dicts to Metric objects."""
        data = {
            "id": "m",
            "name": "M",
            "metrics": [{"name": "score", "value": 0.8, "unit": "acc", "timestamp": ""}],
        }
        e = Experiment.from_dict(data)
        assert isinstance(e.metrics[0], Metric)
        assert e.metrics[0].name == "score"

    def test_to_dict_from_dict_roundtrip(self):
        """Roundtrip preserves data."""
        original = Experiment(
            id="round",
            name="Roundtrip",
            tags=["test"],
            config={"key": "val"},
            created_at="2026-03-01T09:00:00",
        )
        d = original.to_dict()
        reconstructed = Experiment.from_dict(d)
        assert reconstructed.id == original.id
        assert reconstructed.name == original.name
        assert reconstructed.created_at == original.created_at
        assert reconstructed.config == original.config


