class TestRenderList:
    """Test render_list formatting."""

    def _render_list(self, exps, verbose=False):
        """Replicate render_list formatting."""
        if not exps:
            return "No experiments found."

        by_status = {}
        for e in exps:
            by_status[e.status] = by_status.get(e.status, 0) + 1
        total = len(exps)
        status_parts = "  |  ".join(f"{s}: {c}" for s, c in sorted(by_status.items()))
        summary = f"Total: {total}  |  {status_parts}"

        lines = [summary, ""]
        icons = {"running": "⚡", "completed": "✓", "failed": "✗"}

        for e in exps:
            icon = icons.get(e.status, "?")
            lines.append(f"{icon} [{e.id}] {e.name} ({e.status})")
            if e.roadmap_milestone:
                lines.append(f"  Milestone: {e.roadmap_milestone}")
            if verbose and e.metrics:
                metric_str = ", ".join(f"{m.name}={m.value}" for m in e.metrics)
                lines.append(f"  Metrics: {metric_str}")

        return "\n".join(lines)

    def test_empty_returns_placeholder(self):
        """Empty list returns placeholder."""
        assert self._render_list([]) == "No experiments found."

    def test_summary_counts_by_status(self):
        """Summary shows total and counts per status."""
        exps = [
            Experiment(id="1", name="E1", status="running"),
            Experiment(id="2", name="E2", status="running"),
            Experiment(id="3", name="E3", status="completed"),
        ]
        output = self._render_list(exps)
        assert "Total: 3" in output
        assert "running: 2" in output
        assert "completed: 1" in output

    def test_status_icons(self):
        """Status icons are shown."""
        exps = [
            Experiment(id="1", name="R", status="running"),
            Experiment(id="2", name="C", status="completed"),
            Experiment(id="3", name="F", status="failed"),
        ]
        output = self._render_list(exps)
        assert "⚡" in output  # running
        assert "✓" in output  # completed
        assert "✗" in output  # failed

    def test_milestone_shown(self):
        """Roadmap milestone is displayed."""
        exps = [
            Experiment(id="1", name="E", roadmap_milestone="Phase 1: Baseline"),
        ]
        output = self._render_list(exps)
        assert "Phase 1: Baseline" in output

    def test_verbose_shows_metrics(self):
        """Verbose mode shows metrics."""
        exps = [
            Experiment(id="1", name="E", metrics=[
                Metric(name="accuracy", value=0.95),
                Metric(name="loss", value=0.05),
            ]),
        ]
        output = self._render_list(exps, verbose=True)
        assert "accuracy=0.95" in output
        assert "loss=0.05" in output

    def test_non_verbose_excludes_metrics(self):
        """Non-verbose mode hides metrics."""
        exps = [
            Experiment(id="1", name="E", metrics=[Metric(name="acc", value=0.9)]),
        ]
        output = self._render_list(exps, verbose=False)
        assert "Metrics:" not in output

    def test_sorted_by_status_part(self):
        """Status parts are sorted alphabetically."""
        exps = [
            Experiment(id="1", name="E1", status="completed"),
            Experiment(id="2", name="E2", status="running"),
            Experiment(id="3", name="E3", status="failed"),
        ]
        output = self._render_list(exps)
        # sorted gives: cancelled, completed, failed, running
        assert output.index("completed: 1") < output.index("failed: 1")
        assert output.index("failed: 1") < output.index("running: 1")


