class TestRenderCompare:
    """Test render_compare formatting."""

    def _render_compare(self, comp):
        """Replicate render_compare formatting."""
        if "error" in comp:
            return f"Error: {comp['error']}"

        metrics_header = " | ".join(comp["metrics"])
        sep = "|---" + "|---".join([""] * len(comp["metrics"]))

        lines = [
            "## Experiment Comparison",
            "",
            f"| Exp | {metrics_header} |",
            f"|---| {sep} |",
        ]

        for r in comp["experiments"]:
            vals = [str(r.get(m, "-")) for m in comp["metrics"]]
            name_truncated = r["name"][:15]
            lines.append(f"| {name_truncated} | " + " | ".join(vals) + " |")

        return "\n".join(lines)

    def test_error_returns_error_message(self):
        """Error comp returns formatted error."""
        result = self._render_compare({"error": "No experiments found"})
        assert result == "Error: No experiments found"

    def test_header_row(self):
        """Table has header row with metric names."""
        comp = {"metrics": ["accuracy", "f1"], "experiments": []}
        output = self._render_compare(comp)
        assert "| Exp | accuracy | f1 |" in output

    def test_experiment_row_name_truncated_to_15(self):
        """Experiment name truncated to 15 chars."""
        comp = {
            "metrics": ["acc"],
            "experiments": [{"id": "1", "name": "A" * 20, "status": "r", "acc": 0.9}],
        }
        output = self._render_compare(comp)
        assert ("A" * 15) in output
        assert ("A" * 20) not in output

    def test_missing_metric_shows_dash(self):
        """Missing metric value shows dash."""
        comp = {
            "metrics": ["acc", "f1"],
            "experiments": [{"id": "1", "name": "E", "status": "r", "acc": 0.9}],
        }
        output = self._render_compare(comp)
        # Find the data row (starts with "| E |")
        row = [l for l in output.split("\n") if l.startswith("| E |")][0]
        assert "| 0.9 | - |" in row

    def test_multiple_experiments(self):
        """Multiple experiments each on their own row."""
        comp = {
            "metrics": ["acc"],
            "experiments": [
                {"id": "1", "name": "E1", "status": "r", "acc": 0.9},
                {"id": "2", "name": "E2", "status": "r", "acc": 0.85},
            ],
        }
        output = self._render_compare(comp)
        assert "E1" in output
        assert "E2" in output


